package Model;

/**
 *
 * @author macair
 */
public class Meal {
    
    private int id ;
    private String name ;
    private double rate ;
    private double price;
    private Category category ;

    public Meal(int id, String name, double rate, double price, Category category) {
        this.id = id;
        this.name = name;
        this.rate = rate;
        this.price = price;
        this.category = category;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getRate() {
        return rate;
    }

    public void setRate(double rate) {
        this.rate = rate;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public Category getCategory() {
        return category;
    }

    public void setCategory(Category category) {
        this.category = category;
    }
        
}


